float ia_min(float a, float b) { return (a<b)?a:b; }

float ia_max(float a, float b) { return (a>b)?a:b; }

double ia_min(double a, double b) { return a<b?a:b; }

double ia_max(double a, double b) { return a>b?a:b; }

